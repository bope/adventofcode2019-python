from aoc.day12 import part1


def check_moons(moons, expected):
    cmp = ""
    for moon in moons:
        cmp += "pos=<x={:3d}, y={:3d}, z={:3d}>, vel=<x={:3d}, y={:3d}, z={:3d}>\n".format(
            moon.pos.x,
            moon.pos.y,
            moon.pos.z,
            moon.vel.x,
            moon.vel.y,
            moon.vel.z
        )

    assert cmp.strip() == expected.strip()


